#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (i < (size - 1) && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	if (i < size)
	{
		dst[i] = '\0';
	}
	while (src[i] != '\0')
		i++;
	if (size < ft_strlen(src))
		return (ft_strlen(src));
	if (ft_strlen(src) == 0)
		return (ft_strlen(src));
	return (i);
}
